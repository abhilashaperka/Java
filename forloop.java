public class forloop {
    public static void main(String[] args){
        for (int i=0;i<=5;i++){
            System.out.println("hi "+i);            
        }
        for (int j=5;j>=1;j--){
            System.out.println("hello"+j);            
        }
        //exmpl
        int z=5;
         for (;z>=1;){
            System.out.println("namasthe!! "+z);     
            z--;       
        }
    }    
}
