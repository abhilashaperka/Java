class stringexmpl{
    public static void main(String args[]){
        //string example-stores stream of characters
        String name="Abhilasha";
        System.out.println("name : "+name);
        System.out.println("hashcode : "+name.hashCode());//a numeric representation of a word used by computers for fast searching and comparison.
        System.out.println("character at index 3 is : "+name.charAt(3));
        //concatenation of string
        System.out.println("Perka "+name );
        System.out.println(name.concat(" Perka"));

    }
}
